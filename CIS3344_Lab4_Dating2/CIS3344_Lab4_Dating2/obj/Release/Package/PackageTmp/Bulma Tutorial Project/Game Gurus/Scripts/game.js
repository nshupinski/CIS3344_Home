class Game {
    constructor(id, title, rating, genre, image, releaseDate, developer, description) {
        this.id = id;
        this.title = title;
        this.rating = rating;
        this.genre = genre;
        this.image = image;
        this.releaseDate = releaseDate;
        this.developer = developer;
        this.description = description;
    }
}